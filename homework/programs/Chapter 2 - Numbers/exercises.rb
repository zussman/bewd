# A Few Things to Try
# Write a program that tells you the following:
# 1. Hours in a year. How many hours are in a year?
print "How many hours are in a year? "
puts 24 * 365

# 2. Minutes in a decade. 
print "How many minutes are in a decade? "
puts 60 * 24 * 365 * 10

# 3. Your age in seconds.
print "How many seconds old are you? "
puts (60 * 60 * 24 * 365 * 27) - (33 * 24 * 60 * 60) + (16 * 60 * 60)

# 4. Our dear author's age.
print "Our dear author's age. If he is 1,025 million seconds old..."
puts 1025000000 / 60 / 60 / 24 / 365